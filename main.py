def add(a,b):
    return a+b
def sub(a,b):
    return a-b
def mul(a,b):
    return a*b
def divi(a,b):
    return a/b

x=int(input('Enter first number: '))
y=int(input('Enter second number: '))
print('addition=',add(x,y))
print('subtraction=',sub(x,y))
print('multiplication=',mul(x,y))
print('division=',divi(x,y))